package zeniware.admin.codeman.vo;

public class CommonGroup {
  
  private String value;
  private String text;
  
  public CommonGroup() { }
  
  public String getValue() {
    return this.value;
  }
  
  public void setValue(String value) {
    this.value = value;
  }
  
  public String getText() {
    return this.text;
  }
  
  public void setText(String text) {
    this.text = text;
  }

}
